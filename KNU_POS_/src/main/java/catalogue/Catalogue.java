package catalogue;

public class Catalogue {
	private int catalogueID;
	private String catalogueName;
	private String cataloguePrice;
	private int catalogueCount;
	public int getCatalogueID() {
		return catalogueID;
	}
	public void setCatalogueID(int catalogueID) {
		this.catalogueID = catalogueID;
	}
	public String getCatalogueName() {
		return catalogueName;
	}
	public void setCatalogueName(String catalogueName) {
		this.catalogueName = catalogueName;
	}
	public String getCataloguePrice() {
		return cataloguePrice;
	}
	public void setCataloguePrice(String cataloguePrice) {
		this.cataloguePrice = cataloguePrice;
	}
	public int getCatalogueCount() {
		return catalogueCount;
	}
	public void setCatalogueCount(int catalogueCount) {
		this.catalogueCount = catalogueCount;
	}
	
	
}
